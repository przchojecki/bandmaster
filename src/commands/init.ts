type PolicyMode = "auto" | "safe-only" | "manual";
type StopMode = "drain" | "hard";
type PermissionScope = "delegated-safe" | "delegated-full" | "manual";
type AuthMode = "api" | "subscription";

interface InitCommandOptions {
  config?: string;
  cwd?: string;
}

interface ModelRef {
  provider: ProviderName;
  model: string;
}

