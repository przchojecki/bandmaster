async function pathExists(targetPath: string): Promise<boolean> {
  try {
    await access(targetPath);
    return true;
  } catch {
    return false;
  }
}

function validatePositiveInteger(rawValue: string): string | undefined {
  if (!/^\d+$/.test(rawValue)) {
    return "Enter a positive integer.";
  }
  const parsed = Number.parseInt(rawValue, 10);
  if (!Number.isSafeInteger(parsed) || parsed <= 0) {
    return "Enter a positive integer.";
  }
  return undefined;
}

function getFallbackDefault(primary: ProviderName, preferred: ProviderName): ProviderName {
  if (primary !== preferred) {
    return preferred;
  }
  const alternate = PROVIDERS.find((provider) => provider !== primary);
  return alternate ?? primary;
}

function uniqueProviders(providers: ProviderName[]): ProviderName[] {
  const set = new Set<ProviderName>(providers);
  return Array.from(set);
}

function formatModel(ref: ModelRef): string {
  return `${ref.provider}/${ref.model}`;
}

function formatConnection(config: ProjectConfig, provider: ProviderName): string {
  const auth = config.providers[provider]?.auth;
  if (!auth) {
    return "local";
  }
  if (auth.mode === "api") {
    return `API (${auth.apiKeyEnv ?? "missing-env"})`;
  }
  return `Subscription (${auth.subscriptionCommand ?? "missing-command"})`;
}

function formatRoleSummary(
  primary: ModelRef,
  fallback: ModelRef[],
  config: ProjectConfig
): string {
  const primaryText = `${formatModel(primary)} | ${formatConnection(config, primary.provider)}`;
  if (fallback.length === 0) {
    return primaryText;
  }
  const fallbackText = fallback
    .map((item) => `${formatModel(item)} | ${formatConnection(config, item.provider)}`)
    .join(", ");
  return `${primaryText}; fallback: ${fallbackText}`;
}

export async function initCommand(options: InitCommandOptions): Promise<void> {
  if (!process.stdin.isTTY || !process.stdout.isTTY) {
    throw new Error("Interactive init requires a TTY terminal.");
  }

  const cwd = options.cwd ?? process.cwd();
  const configPath = path.resolve(cwd, options.config ?? ".bandmaster/project.toml");
  const rl = createInterface({ input, output });

  const askText = async (
    question: string,
    defaultValue: string,
    validate?: (value: string) => string | undefined
  ): Promise<string> => {
    while (true) {
      const answer = (await rl.question(`${question} [${defaultValue}]: `)).trim();
      const value = answer.length > 0 ? answer : defaultValue;
      const message = validate ? validate(value) : undefined;
      if (!message) {
        return value;
      }
      console.log(message);
    }
  };

  const askChoice = async <T extends string>(
    question: string,
    optionsList: readonly T[],
    defaultValue: T
  ): Promise<T> => {
    while (true) {
      console.log(question);
      optionsList.forEach((option, index) => {
        const marker = option === defaultValue ? " (default)" : "";
        console.log(`  ${index + 1}. ${option}${marker}`);
      });
      const answer = (
        await rl.question(`Choose 1-${optionsList.length} [${defaultValue}]: `)
      ).trim();
      if (answer.length === 0) {
        return defaultValue;
      }
      const parsed = Number.parseInt(answer, 10);
      if (Number.isInteger(parsed) && parsed >= 1 && parsed <= optionsList.length) {
        const selected = optionsList[parsed - 1];
        if (selected !== undefined) {
          return selected;
        }
      }
      console.log("Enter a valid option number.");
    }
  };

  const askBoolean = async (question: string, defaultValue: boolean): Promise<boolean> => {
    const suffix = defaultValue ? "[Y/n]" : "[y/N]";
    while (true) {
      const answer = (await rl.question(`${question} ${suffix}: `)).trim().toLowerCase();
      if (answer.length === 0) {
        return defaultValue;
      }
      if (answer === "y" || answer === "yes") {
        return true;
      }
      if (answer === "n" || answer === "no") {
        return false;
      }
      console.log("Please answer y or n.");
    }
  };

  const askPositiveInt = async (
    question: string,
    defaultValue: number
  ): Promise<number> => {
    const raw = await askText(question, String(defaultValue), validatePositiveInteger);
    return Number.parseInt(raw, 10);
  };

  const askMenuChoice = async (min: number, max: number): Promise<number> => {
    while (true) {
      const answer = (await rl.question(`Choose ${min}-${max}: `)).trim();
      const parsed = Number.parseInt(answer, 10);
      if (Number.isInteger(parsed) && parsed >= min && parsed <= max) {
        return parsed;
      }
      console.log("Enter a valid option number.");
    }
  };

  const runExternalCommand = async (command: string): Promise<number> =>
    new Promise((resolve) => {
      const child = spawn(command, {
        stdio: "inherit",
        shell: true
      });
      child.on("error", () => resolve(1));
      child.on("exit", (code) => resolve(code ?? 1));
    });

  const getCommandBinary = (command: string): string => {
    const [binary] = command.trim().split(/\s+/);
    return binary ?? "";
  };

  const isBinaryAvailable = async (binary: string): Promise<boolean> => {
    if (!binary) {
      return false;
    }
    return new Promise((resolve) => {
      const child = spawn("sh", ["-lc", `command -v ${binary} >/dev/null 2>&1`], {
        stdio: "ignore"
      });
      child.on("error", () => resolve(false));
      child.on("exit", (code) => resolve(code === 0));
    });
  };

  const configureProviderConnection = async (
    draft: ProjectConfig,
    provider: ProviderName
  ): Promise<void> => {
    if (provider === "ollama") {
      delete draft.providers[provider];
      console.log(`${provider} uses local runtime. No API/subscription setup required.`);
      return;
    }

    const currentAuth = draft.providers[provider]?.auth;
    const defaultModeLabel = "Subscription";
    const modeLabel = await askChoice(
      `Choose ${provider} connection mode:`,
      ["API", "Subscription"] as const,
      defaultModeLabel
    );
    const mode: AuthMode = modeLabel === "API" ? "api" : "subscription";

    if (mode === "api") {
      const apiKeyEnv = await askText(
        `${provider} API key env var`,
        currentAuth?.mode === "api"
          ? (currentAuth.apiKeyEnv ?? DEFAULT_API_ENV_BY_PROVIDER[provider] ?? "API_KEY")
          : (DEFAULT_API_ENV_BY_PROVIDER[provider] ?? "API_KEY"),
        (value) => (value.length > 0 ? undefined : "Env var name is required.")
      );
      draft.providers[provider] = {
        auth: {
          mode: "api",
          apiKeyEnv
        }
      };
      return;
    }

    const selectedCommand = SUBSCRIPTION_AUTH_COMMAND[provider];
    const binary = getCommandBinary(selectedCommand);
    if (await isBinaryAvailable(binary)) {
      console.log(`Starting ${provider} subscription authentication: ${selectedCommand}`);
      rl.pause();
      const exitCode = await runExternalCommand(selectedCommand);
      rl.resume();
      if (exitCode === 0) {
        console.log("Authentication command finished successfully.");
      } else {
        console.log(
          `Authentication command exited with code ${exitCode}. You can rerun it manually: ${selectedCommand}`
        );
      }
    } else {
      console.log(
        `Could not find "${binary}" on PATH. Install the provider CLI and run: ${selectedCommand}`
      );
    }

    draft.providers[provider] = {
      auth: {
        mode: "subscription",
        subscriptionCommand: selectedCommand
      }
    };
  };

  const configureWorker = async (draft: ProjectConfig): Promise<void> => {
    console.log("\nConfigure worker LLM");
    const primaryProvider = await askChoice(
      "Select worker provider:",
      PROVIDERS,
      draft.worker.primary.provider
    );
    const primaryModel = await askText(
      "Worker model",
      draft.worker.primary.provider === primaryProvider
        ? draft.worker.primary.model
        : DEFAULT_MODEL_BY_PROVIDER[primaryProvider],
      (value) => (value.length > 0 ? undefined : "Worker model is required.")
    );

    const hasFallback = draft.worker.fallback.length > 0;
    const useFallback = await askBoolean("Configure worker fallback?", hasFallback);
    const fallback: ModelRef[] = [];
    if (useFallback) {
      const currentFallback = draft.worker.fallback[0];
      const fallbackProvider = await askChoice(
        "Select worker fallback provider:",
        PROVIDERS,
        currentFallback?.provider ??
          getFallbackDefault(primaryProvider, "claude")
      );
      const fallbackModel = await askText(
        "Worker fallback model",
        currentFallback?.provider === fallbackProvider
          ? currentFallback.model
          : DEFAULT_MODEL_BY_PROVIDER[fallbackProvider],
        (value) => (value.length > 0 ? undefined : "Fallback model is required.")
      );
      fallback.push({
        provider: fallbackProvider,
        model: fallbackModel
      });
    }

    draft.worker.primary = {
      provider: primaryProvider,
      model: primaryModel
    };
    draft.worker.fallback = fallback;

    for (const provider of uniqueProviders([
      primaryProvider,
      ...fallback.map((item) => item.provider)
    ])) {
      await configureProviderConnection(draft, provider);
    }
  };

  const configureManager = async (draft: ProjectConfig): Promise<void> => {
    console.log("\nConfigure manager LLM");
    const primaryProvider = await askChoice(
      "Select manager provider:",
      PROVIDERS,
      draft.pm.primary.provider
    );
    const primaryModel = await askText(
      "Manager model",
      draft.pm.primary.provider === primaryProvider
        ? draft.pm.primary.model
        : DEFAULT_MODEL_BY_PROVIDER[primaryProvider],
      (value) => (value.length > 0 ? undefined : "Manager model is required.")
    );

    const hasFallback = draft.pm.fallback.length > 0;
    const useFallback = await askBoolean("Configure manager fallback?", hasFallback);
    const fallback: ModelRef[] = [];
    if (useFallback) {
      const currentFallback = draft.pm.fallback[0];
      const fallbackProvider = await askChoice(
        "Select manager fallback provider:",
        PROVIDERS,
        currentFallback?.provider ??
          getFallbackDefault(primaryProvider, "gemini")
      );
      const fallbackModel = await askText(
        "Manager fallback model",
        currentFallback?.provider === fallbackProvider
          ? currentFallback.model
          : DEFAULT_MODEL_BY_PROVIDER[fallbackProvider],
        (value) => (value.length > 0 ? undefined : "Fallback model is required.")
      );
      fallback.push({
        provider: fallbackProvider,
        model: fallbackModel
      });
    }

    const userProxyEnabled = await askBoolean(
      "Enable manager as user proxy?",
      draft.pm.userProxy.enabled
    );
    const permissionScope = userProxyEnabled
      ? await askChoice(
          "User-proxy permission scope:",
          ["delegated-safe", "delegated-full", "manual"] as const,
          draft.pm.userProxy.permissionScope as PermissionScope
        )
      : "manual";
    const guidanceStyle = await askText(
      "Manager guidance style",
      draft.pm.userProxy.guidanceStyle,
      (value) => (value.length > 0 ? undefined : "Guidance style is required.")
    );

    draft.pm.primary = {
      provider: primaryProvider,
      model: primaryModel
    };
    draft.pm.fallback = fallback;
    draft.pm.userProxy = {
      enabled: userProxyEnabled,
      permissionScope,
      guidanceStyle
    };

    for (const provider of uniqueProviders([
      primaryProvider,
      ...fallback.map((item) => item.provider)
    ])) {
      await configureProviderConnection(draft, provider);
    }
  };

  const configureSettings = async (draft: ProjectConfig): Promise<void> => {
    console.log("\nSettings");
    draft.policy.mode = (await askChoice(
      "Permission policy mode:",
      ["safe-only", "auto", "manual"] as const,
      draft.policy.mode as PolicyMode
    )) as PolicyMode;
    draft.checkpoint.intervalMinutes = await askPositiveInt(
      "Checkpoint interval (minutes)",
      draft.checkpoint.intervalMinutes
    );
    draft.dialogue.interveneOnStall = await askBoolean(
      "Allow manager intervention on stalls?",
      draft.dialogue.interveneOnStall
    );
    if (draft.dialogue.interveneOnStall) {
      draft.dialogue.stallMinutes = await askPositiveInt(
        "Stall threshold (minutes)",
        draft.dialogue.stallMinutes
      );
    }
    draft.budget.maxMinutes = await askPositiveInt(
      "Session max minutes",
      draft.budget.maxMinutes
    );
    draft.budget.maxTokens = await askPositiveInt(
      "Session max tokens",
      draft.budget.maxTokens
    );
    draft.budget.maxTurns = await askPositiveInt(
      "Session max turns",
      draft.budget.maxTurns
    );
    draft.budget.cycle.maxTokens = await askPositiveInt(
      "Cycle max tokens",
      draft.budget.cycle.maxTokens
    );
    draft.budget.cycle.maxTurns = await askPositiveInt(
      "Cycle max turns",
      draft.budget.cycle.maxTurns
    );
    draft.budget.stopMode = (await askChoice(
      "Stop mode at budget end:",
      ["drain", "hard"] as const,
      draft.budget.stopMode as StopMode
    )) as StopMode;
  };

  const runAiWorkAndSave = async (draft: ProjectConfig): Promise<boolean> => {
    console.log("\nRun AI work");
    draft.project.name = await askText(
      "Project name",
      draft.project.name,
      (value) => (value.length > 0 ? undefined : "Project name is required.")
    );
    draft.project.objective = await askText(
      "Project objective",
      draft.project.objective,
      (value) => (value.length > 0 ? undefined : "Project objective is required.")
    );
    draft.project.workspace = await askText(
      "Workspace path",
      draft.project.workspace,
      (value) => (value.length > 0 ? undefined : "Workspace path is required.")
    );
    draft.project.entryDocs = normalizeEntryDocs(
      await askText(
        "Entry docs (comma-separated)",
        draft.project.entryDocs.join(", "),
        (value) =>
          normalizeEntryDocs(value).length > 0
            ? undefined
            : "At least one entry doc is required."
      )
    );

    const parsed = ProjectConfigSchema.parse(draft);
    const exists = await pathExists(configPath);
    if (exists) {
      const overwrite = await askBoolean(
        `Config already exists at ${configPath}. Overwrite?`,
        false
      );
      if (!overwrite) {
        console.log("Run setup cancelled. Existing config left unchanged.");
        return false;
      }
    }

    await mkdir(path.dirname(configPath), { recursive: true });
    await writeFile(configPath, toProjectToml(parsed), "utf8");

    console.log("\nWrote project config:");
    console.log(configPath);
    console.log("\nStarting run with saved config...\n");
    await runCommand({ config: configPath, cwd });
    return true;
  };

  try {
    let draft = createDefaultConfig(cwd);
    if (await pathExists(configPath)) {
      try {
        const loaded = await loadProjectConfig({ configPath, cwd });
        draft = loaded.config;
      } catch (error) {
        const detail = error instanceof Error ? error.message : String(error);
        console.warn(`Could not load existing config at ${configPath}. Using defaults.`);
        console.warn(detail);
      }
    }

    console.log(BANDMASTER_BANNER);
    console.log("\nBandmaster Interactive Menu");

    let finished = false;
    while (!finished) {
      console.log("\nMain Menu");
      console.log(
        `1. Configure worker LLM (${formatRoleSummary(draft.worker.primary, draft.worker.fallback, draft)})`
      );
      console.log(
        `2. Configure manager LLM (${formatRoleSummary(draft.pm.primary, draft.pm.fallback, draft)})`
      );
      console.log(
        `3. Settings (policy=${draft.policy.mode}, checkpoint=${draft.checkpoint.intervalMinutes}m, session=${draft.budget.maxMinutes}m, cycle=${draft.budget.cycle.maxTokens}t)`
      );
      console.log("4. Run AI work (project/docs + save + run)");
      console.log("5. Exit without saving");

      const choice = await askMenuChoice(1, 5);

      if (choice === 1) {
        await configureWorker(draft);
        continue;
      }
      if (choice === 2) {
        await configureManager(draft);
        continue;
      }
      if (choice === 3) {
        await configureSettings(draft);
        continue;
      }
      if (choice === 4) {
        finished = await runAiWorkAndSave(draft);
        continue;
      }

      console.log("Exited without saving.");
      finished = true;
    }
  } finally {
    rl.close();
  }
}
